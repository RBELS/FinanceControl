package com.example.financecontrol.dbmodels;

/**
 * ResponseItem class which creates objects that contains current values
 */
public class ResponseItem {
    /**
     * Cur_ID - an int object which contains your current ID
     */
    public int Cur_ID;
    /**
     * Date - a string object which contains current date
     */
    public String Date;
    /**
     * Cur_Abbreviation - a string object which contains current abbreviation of currency
     */
    public String Cur_Abbreviation;
    /**
     * Cur_Scale - an int object which contains current scale
     */
    public int Cur_Scale;
    /**
     * Cur_Name - a string object which contains current name of operation
     */
    public String Cur_Name;
    /**
     * Cur_OfficialRate - a double object which contains current official rate of currency
     */
    public double Cur_OfficialRate;
}
